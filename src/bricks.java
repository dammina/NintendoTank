
public class bricks {
	private int x;
	private int y;
	private int damageLevel;
	public bricks(int x, int y, int damageLevel) {
		super();
		this.x = x;
		this.y = y;
		this.damageLevel = damageLevel;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getDamageLevel() {
		return damageLevel;
	}
	public void setDamageLevel(int damageLevel) {
		this.damageLevel = damageLevel;
	}
	

}
